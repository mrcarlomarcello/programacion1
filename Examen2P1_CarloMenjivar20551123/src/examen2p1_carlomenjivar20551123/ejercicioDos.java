    public int [] calcularSoledad(int[] soledad){
        
        // [posicion, sumatoriasoledad]
        int[] Soledad = new int[2];
        
        int sumatoriaSoledad = 0;
        int sumatoriaComparar = 0;
        
        for(int i = 0; i < soledad.length; i++){
            sumatoriaComparar = sumatoriaSoledad(i, soledad);
            System.out.println(sumatoriaComparar);
            
            if(i == 0){
                sumatoriaSoledad = sumatoriaComparar;
                Soledad[0]=i;
                Soledad[1]=sumatoriaSoledad;
            }else{
                if(sumatoriaComparar <= sumatoriaSoledad){
                    sumatoriaSoledad = sumatoriaComparar;
                    Soledad[0]=i;
                    Soledad[1]=sumatoriaSoledad;
                }
            }   
        }
        
        return Soledad;
    }
    
    public int sumatoriaSoledad(int pos, int [] soledad){
        
        int sumatoria = soledad[pos];
        
        int min;
        int max;
        
        if((pos-soledad[pos])<0){
            min = 0;
        }else{
            min=pos-soledad[pos];
        }
        
        if((pos+soledad[pos])>=soledad.length){
            max=soledad.length-1;
        }else{
            max=pos+soledad[pos];
        }
        
        for(int i = pos-1; i>=min; i--){
            sumatoria+=soledad[i];
        }
        
        for(int i= pos+1; i<=max; i++){
            sumatoria+=soledad[i];
        }
        
        return sumatoria;
    }

